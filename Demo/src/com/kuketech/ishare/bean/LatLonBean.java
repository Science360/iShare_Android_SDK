package com.kuketech.ishare.bean;

public class LatLonBean {
	private String lat;
	private String lon;
	private String city;

	public LatLonBean() {
		super();
	}

	public String getLat() {
		return lat;
	}

	public void setLat(String lat) {
		this.lat = lat;
	}

	public String getLon() {
		return lon;
	}

	public void setLon(String lon) {
		this.lon = lon;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

}
